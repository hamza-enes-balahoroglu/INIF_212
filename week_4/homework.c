void add_product(Prime_Product_Holder *holder, Prime_Product new_product) {

  if (holder->size == holder->capacity) {
    size_t newcapacity = (holder->capacity == 0) ? 4 : holder->capacity * 2;

    Prime_Product *temp =
        realloc(holder->products, newcapacity * sizeof(Prime_Product));

    if (temp == NULL) {
      printf("Bellek alani yetersiz.");
      return;
    }

    holder->products = temp;
    holder->capacity = newcapacity;
  }
  printf("\nnew product  added");
  holder->products[holder->size] = new_product;
  holder->size++;
}
