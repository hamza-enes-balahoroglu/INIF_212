void calculate_primes(void) {

  bool mask[MAX_NUMBER + 1];
  memset(&mask, 0, sizeof(mask));

  for (int p = 2; p < MAX_NUMBER; p++) {
    if (mask[p] == false) {

      primes[p] = true;

      if (p <= MAX_NUMBER / p) {
        for (int c = p * p; c <= MAX_NUMBER; c += p) {
          mask[c] = true;
        }
      }
    }
  }
}

